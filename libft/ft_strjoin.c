#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	int		j;
	int		cnt;
	char	*str;

	if (s1 == NULL || s2 == NULL)
		return (NULL);
	cnt = ft_strlen(s1) + ft_strlen(s2);
	str = malloc(sizeof(char) * (cnt + 1));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
		str[i++] = s2[j++];
	str[i] = '\0';
	return (str);
}
